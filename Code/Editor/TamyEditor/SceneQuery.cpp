#include "SceneQuery.h"
#include "QueryRenderingPass.h"


///////////////////////////////////////////////////////////////////////////////

SceneQuery::SceneQuery()
   : m_resultBuf( 0, 0, 0, 0 )
{

}

///////////////////////////////////////////////////////////////////////////////

void SceneQuery::notifyResult()
{
   Entity* entity = reinterpret_cast< Entity* >( QueryRenderingPass::vecToPtr( m_resultBuf ) );
   m_resultBuf = Vector::ZERO;
   setResult( entity );
}

