#include "core-Renderer/DepthBuffer.h"
#include "core-Renderer/Renderer.h"


///////////////////////////////////////////////////////////////////////////////

DepthBuffer::DepthBuffer( uint width, uint height )
   : m_width( width )
   , m_height( height )
{
}

///////////////////////////////////////////////////////////////////////////////

DepthBuffer::~DepthBuffer()
{
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

RCActivateDepthBuffer::RCActivateDepthBuffer( DepthBuffer* depthBuffer ) 
   : m_depthBuffer( depthBuffer ) 
{}

///////////////////////////////////////////////////////////////////////////////

void RCActivateDepthBuffer::execute( Renderer& renderer )
{
   renderer.pushDepthBuffer( m_depthBuffer );
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

RCDeactivateDepthBuffer::RCDeactivateDepthBuffer( DepthBuffer* depthBuffer ) 
   : m_depthBuffer( depthBuffer ) 
{}

///////////////////////////////////////////////////////////////////////////////

void RCDeactivateDepthBuffer::execute( Renderer& renderer )
{
   renderer.popDepthBuffer( m_depthBuffer );
}

///////////////////////////////////////////////////////////////////////////////
