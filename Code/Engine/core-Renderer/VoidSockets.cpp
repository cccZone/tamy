///////////////////////////////////////////////////////////////////////////////

bool RPVoidInput::canConnect( RPNodeOutput& output ) const
{
   RPVoidOutput* typedOutput = dynamic_cast< RPVoidOutput* >( &output );
   return typedOutput != NULL; 
}

