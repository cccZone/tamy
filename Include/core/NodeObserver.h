#pragma once


///////////////////////////////////////////////////////////////////////////////

class Node;

///////////////////////////////////////////////////////////////////////////////

/**
 * This observer will track the nodes that are added and removed to a node
 */
class NodeObserver
{
public:
   virtual ~NodeObserver() {}

   virtual void childAdded(Node& parent, Node& child) = 0;

   virtual void childRemoved(Node& parent, Node& child) = 0;
};

///////////////////////////////////////////////////////////////////////////////
