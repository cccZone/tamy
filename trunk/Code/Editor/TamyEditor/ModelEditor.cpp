#include "ModelEditor.h"
#include "core-MVC.h"
#include "core.h"


///////////////////////////////////////////////////////////////////////////////

ModelEditor::ModelEditor( Model& scene )
: m_scene( scene )
{
}

///////////////////////////////////////////////////////////////////////////////

void ModelEditor::addEntity( const SerializableReflectionType& entityClass )
{
   Entity* newEntity = entityClass.instantiate< Entity >();
   m_scene.add( newEntity );
}

///////////////////////////////////////////////////////////////////////////////

void ModelEditor::removeEntity( Entity* entity )
{
   m_scene.remove( *entity );
}

///////////////////////////////////////////////////////////////////////////////

void ModelEditor::clearEntity()
{
   m_scene.clear();
}

