#include "core\Property.h"


///////////////////////////////////////////////////////////////////////////////

Property::Property()
: m_observer(NULL)
{
}

///////////////////////////////////////////////////////////////////////////////

void Property::setObserver(PropertyObserver& observer)
{
   m_observer = &observer;
}

///////////////////////////////////////////////////////////////////////////////

PropertyObserver& Property::getObserver()
{
   return *m_observer;
}

///////////////////////////////////////////////////////////////////////////////

void Property::notifyAboutChange()
{
   if ( m_observer )
   {
      m_observer->onPropertyChanged(*this);
   }
}

