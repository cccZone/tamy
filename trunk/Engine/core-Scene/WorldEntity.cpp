#include "core-Scene\WorldEntity.h"
#include "core\Serializer.h"


///////////////////////////////////////////////////////////////////////////////

DEFINE_ENTITY(WorldEntity);

///////////////////////////////////////////////////////////////////////////////

WorldEntity::WorldEntity()
: INIT_ENTITY(WorldEntity)
, m_size(AABoundingBox(D3DXVECTOR3(0, 0, 0), D3DXVECTOR3(0, 0, 0)))
{
}

///////////////////////////////////////////////////////////////////////////////

WorldEntity::WorldEntity(const AABoundingBox& size)
: INIT_ENTITY(WorldEntity)
, m_size(size)
{
}

///////////////////////////////////////////////////////////////////////////////

void WorldEntity::registerProperties()
{
}

///////////////////////////////////////////////////////////////////////////////

void WorldEntity::save(Serializer& serializer)
{
   serializer.saveVec3(m_size.min);
   serializer.saveVec3(m_size.max);
}

///////////////////////////////////////////////////////////////////////////////

void WorldEntity::load(Serializer& serializer)
{
   serializer.loadVec3(m_size.min);
   serializer.loadVec3(m_size.max);
}

///////////////////////////////////////////////////////////////////////////////
