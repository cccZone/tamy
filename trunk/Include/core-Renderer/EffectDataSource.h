#pragma once


///////////////////////////////////////////////////////////////////////////////

class GraphicalEffect;

///////////////////////////////////////////////////////////////////////////////

/** 
 * When the time comes, a GraphicalEffect instance will call the instance
 * of this interface to set the data required by the effect
 */
class EffectDataSource
{
public:
   virtual ~EffectDataSource() {}

   virtual void setData(GraphicalEffect& effect) = 0;
};

