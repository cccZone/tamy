#pragma once


///////////////////////////////////////////////////////////////////////////////

class Managable
{
public:
   virtual ~Managable() {}
};

///////////////////////////////////////////////////////////////////////////////

template<typename T>
class TManagable : public Managable
{
private:
   T* m_object;

public:
   TManagable(T* object) : m_object(object) {}

   ~TManagable() 
   {
      delete m_object; 
      m_object = NULL;
   }
};

///////////////////////////////////////////////////////////////////////////////
