#include "Enemy.h"
#include "RamboScenarioMessages.h"


///////////////////////////////////////////////////////////////////////////////

void Enemy::startShooting(Messagable& enemy)
{
   dispatcher().send(*this, enemy, new Shots(), 0);
}

///////////////////////////////////////////////////////////////////////////////

void Enemy::attack(Messagable& enemy)
{
   dispatcher().send(*this, enemy, new ReduceHealth(), 0);
}

