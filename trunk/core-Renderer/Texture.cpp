#include "core-Renderer\Texture.h"


///////////////////////////////////////////////////////////////////////////////

Texture::Texture(const std::string& name)
      : m_name(name)
{
}

///////////////////////////////////////////////////////////////////////////////

bool Texture::operator==(const Texture& rhs) const
{
   return m_name == rhs.m_name;
}

///////////////////////////////////////////////////////////////////////////////

bool Texture::operator!=(const Texture& rhs) const
{
   return !(*this == rhs);
}

