#include "D3DEmptyTexture.h"


///////////////////////////////////////////////////////////////////////////////

D3DEmptyTexture::D3DEmptyTexture(IDirect3DDevice9& d3Device)
      : Texture(""),
      m_d3Device(d3Device)
{
   m_d3Device.AddRef();
}

///////////////////////////////////////////////////////////////////////////////

D3DEmptyTexture::~D3DEmptyTexture()
{
   m_d3Device.Release();
}

///////////////////////////////////////////////////////////////////////////////

void D3DEmptyTexture::setForRendering()
{
   m_d3Device.SetTexture(0, NULL);
}

